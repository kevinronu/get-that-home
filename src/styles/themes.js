export function GlobalThemeStyle(theme) {
  return `
  body {
    background-color: ${theme.colors.background.standard};
    -webkit-tap-highlight-color: ${theme.colors.primary.standard};
    color: ${theme.colors.text.standard};
  }
  ::selection {
    background-color: ${theme.colors.selection.background};
    color: ${theme.colors.selection.text};
  }
  `;
}

export function isDarkModeActive() {
  return window.matchMedia("(prefers-color-scheme: dark)").matches;
}
